<p>Spedizioni e Resi<br>
    Politica di ritorno e di Exchange<br>
    <br>
    Se per qualsiasi ragione, non sono completamente soddisfatti del vostro acquisto, potete tornare per un periodo di 7 giorni dalla data di arrivo originale. La merce deve essere indossati e nell'imballaggio originale, e accompagnato dalla distinta di imballaggio originale. Faremo anche accettare il ritorno di merce difettosa o non corretto che soddisfa le linee guida qui sotto: <br>
    <br>
       Merce difettosa: include qualsiasi elemento che non funziona nel modo previsto o presenta un danno non associato a normale usura., Ma gli articoli che sono di dimensione errata, il colore o lo stile non è incluso. <br>
      <br>

        Non corretto Merchandise: Contatti via e-mail se hai ricevuto un articolo che è diverso da quello che è stato descritto sul sito o è la dimensione sbagliata, il colore o lo stile di quello che avete ordinato. <br>
      <br>
        <span style="font-weight: bold;">Scambi</span> <br>
      <br>
	    Se hai ricevuto un articolo difettoso o vorrebbero scambiare un oggetto per un altro, semplicemente un nuovo ordine per la voce desiderata e restituire l'elemento difettoso a noi. Come già detto, merce deve essere indossati e nella confezione originale e accompagnato da ricevuta di imballaggio originale. <br>
       <br>
	    A causa del tempo necessario per la spedizione e l'elaborazione della tua ritorno, consigliamo di posizionare un nuovo ordine immediatamente per garantire l'elemento desiderato è in magazzino. Disponibilità di articoli non può stabile a causa di nostre vendite caldo.<br>

      <br>
	    Si prega di notare che se il prezzo del prodotto è cambiato da quando hai effettuato l'ordine originale, saremo lieti di onorare il prezzo precedente sul nuovo ordine - non esitate a contattarci. <br>
